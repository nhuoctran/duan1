// gán +=, -=, *=, /=, %=
console.log("Kết quả phép gán:+=, -=, *=, /=, %=");
let m = 10;
m += 5; // m = m + 5
console.log("Giá trị của m sau phép gán += 5 là: " + m);
let n = 8;
n -= 3; // n = n - 3
console.log("Giá trị của n sau phép gán -= 3 là: " + n);
let p = 6;
p *= 4; // p = p * 4
console.log("Giá trị của p sau phép gán *= 4 là: " + p);
let q = 20;
q /= 5; // q = q / 5
console.log("Giá trị của q sau phép gán /= 5 là: " + q);
let r = 15;
r %= 4; // r = r % 4
console.log("Giá trị của r sau phép gán %= 4 là: " + r);
let a = 5;
let f = 2;
a-=f+=1;
console.log("Giá trị của e sau phép gán e-=f+=1 là: " + a);
// toán tử ++, --
console.log("Kết quả phép toán ++, --");
let b = 10;
b++; // b = b + 1
console.log("Giá trị của b sau phép toán ++ là: " + b);
let c = 7;
c--; // c = c - 1
console.log("Giá trị của c sau phép toán -- là: " + c); 
let d = 5;
console.log("Giá trị của d trước phép toán ++ là: " + d);
console.log("Giá trị của d sau phép toán ++ là: " + ++d); 
